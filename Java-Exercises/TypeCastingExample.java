public class TypeCastingExample {

    public static void main(String[] args) {

        double num1 = 25.89;

        int value = (int) num1;

        System.out.println("Double to Int: " + value);

        int num2 = 50;

        double result = (double) num2;

        System.out.println("Int to Double: " + result);
    }
}